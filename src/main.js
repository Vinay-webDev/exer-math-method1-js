function arithmeticOperations(a, b) {
    let sum = a + b;
    let difference = a - b;
    let product = a * b;
    let quotient = a / b;

    return {
        sum: sum,
        difference: difference,
        product: product,
        quotient: quotient
    };
}

console.log(arithmeticOperations(10, 5));
